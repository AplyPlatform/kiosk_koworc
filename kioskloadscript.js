var onePageCount = 15; // 한 페이지당 출력할 컨텐츠 수

var currentPosition = 0;
var lastPos = contentsData.length - 1;

function loadNextData(curPos) 
{ 
	if (curPos > lastPos) return;
	
	showLoader();
															
	loadNextPageData(curPos);						
	
	hideLoader();	
}
	
function loadNextPageData(curPos)
{
	var contentString = '';
	var p = 0;
	
	
	for (var i=0;i<onePageCount;i++) {
				
			if ( (curPos + i) > lastPos )	break;			
			
			if (i % 5 == 0) 
				contentString += '<div class="box"><a class="leftSide" style="background:url(\'';
			else if ((i % 5) == 4)
				contentString += '<div class="box"><a class="rightSide" style="background:url(\'';
			else 
				contentString += '<div class="box"><a class="inSide" style="background:url(\'';
					
			contentString += contentsData[curPos + i][0];
			contentString += '\'); background-size: 169px Auto; " href="';
			contentString += contentsData[curPos + i][1];
			contentString += '" title="';
			contentString += contentsData[curPos + i][2];
			contentString += '"></a></div>';						
	}
							
	currentPosition = curPos + i;
	
	$('.lastPostsLoader').append(contentString);	
	
	if (currentPosition > lastPos) hideMore();
}

function showMore() {
	$("#loadermore").show();
}

function hideMore() {
	$("#loadermore").hide();
}

function showLoader() {
	$("#loaderbox").show();
}

function hideLoader() {
	$("#loaderbox").hide();
}

$(document).ready(function(){		
	
	showMore();		
	loadNextData(currentPosition);	
				
	$(window).scroll(function(){				
			var docElement = $(document)[0].documentElement;
      var winElement = $(window)[0];

      if ((docElement.scrollHeight - winElement.innerHeight) == winElement.pageYOffset)			
			{								
				loadNextData(currentPosition);
			}								
	});
});

